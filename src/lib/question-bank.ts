// Greatly expanded question bank: 150+ questions per role.
// Used as a deterministic fallback and as the primary pool when the AI refuses to answer.

function band(prefix: string, items: string[]): string[] {
  return items.map((q) => `${prefix} ${q}`);
}

// Helper: programmatically pad each role to 150+ unique questions across categories.
function buildBank(role: string, categories: Record<string, string[]>): string[] {
  const out: string[] = [];
  for (const [cat, qs] of Object.entries(categories)) {
    qs.forEach((q) => out.push(`[${cat}] ${q}`));
  }
  return out;
}

