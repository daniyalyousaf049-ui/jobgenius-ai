export function pickFallbackQuestion(role: string, index: number): string {
  const bank = QUESTION_BANK[role] ?? QUESTION_BANK["Software Engineer"];
  return bank[index % bank.length];
}

// Track asked questions per user/role in localStorage to minimize repeats client-side.
const HISTORY_KEY = "interview-asked-history";
type History = Record<string, Record<string, number>>; // role -> { question: timesAsked }

function loadHistory(): History {
  if (typeof window === "undefined") return {};
  try { return JSON.parse(localStorage.getItem(HISTORY_KEY) || "{}"); } catch { return {}; }
}
function saveHistory(h: History) {
  if (typeof window === "undefined") return;
  localStorage.setItem(HISTORY_KEY, JSON.stringify(h));
}

export function pickLeastAskedQuestion(role: string): { question: string; mastered: boolean } {
  const bank = QUESTION_BANK[role] ?? QUESTION_BANK["Software Engineer"];
  const hist = loadHistory();
  const roleHist = hist[role] || {};
  // sort ascending by timesAsked, take 5 least-asked
  const sorted = [...bank].sort((a, b) => (roleHist[a] || 0) - (roleHist[b] || 0));
  const totalAsked = Object.values(roleHist).reduce((a, b) => a + b, 0);
  const mastered = totalAsked > 0 && totalAsked % bank.length === 0 && sorted.every((q) => (roleHist[q] || 0) >= 1);
  if (mastered) {
    // reset
    hist[role] = {};
    saveHistory(hist);
  }
  const pool = sorted.slice(0, 5);
  const pick = pool[Math.floor(Math.random() * pool.length)];
  hist[role] = { ...(hist[role] || {}), [pick]: (roleHist[pick] || 0) + 1 };
  saveHistory(hist);
  return { question: pick, mastered };
}

export function getMasteryProgress(role: string): { asked: number; total: number } {
  const bank = QUESTION_BANK[role] ?? [];
  const hist = loadHistory();
  const roleHist = hist[role] || {};
  return { asked: Object.keys(roleHist).length, total: bank.length };
}
